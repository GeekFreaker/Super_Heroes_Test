package check.out.superheroes.Models;

public class Images {
    private String xs;
    private String sm;
    private String md;
    private String lg;


    // Getter Methods

    public String getXs() {
        return xs;
    }

    public String getSm() {
        return sm;
    }

    public String getMd() {
        return md;
    }

    public String getLg() {
        return lg;
    }

    // Setter Methods

    public void setXs( String xs ) {
        this.xs = xs;
    }

    public void setSm( String sm ) {
        this.sm = sm;
    }

    public void setMd( String md ) {
        this.md = md;
    }

    public void setLg( String lg ) {
        this.lg = lg;
    }
}
